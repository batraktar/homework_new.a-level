#Кожен пише суму списку за допомогою for та while

ls1 = int(input("Від якого? \n"))
ls2 = int(input("До якого? \n"))
sl = 0

for i in range(ls1, ls2+1):
    print(i, end=' ')
    sl += i
print(f"\nЇх сума: {sl}")

